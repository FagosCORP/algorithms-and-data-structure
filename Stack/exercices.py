# Permite o acesso a um item de dados: o último item inserido.
# Se o último item for removido, o item anterior inserido podera ser acessado
# Aplicações
#  Correção de expressoes aritiméticas tais como 3 * (4+ 5)
#  Percorrimento da arvore binaria
#  Pesquisa de arvore em grafo
#  Microprocessadores com arquitetura baseada em pilhas, Quando um metodo e chamado, seu endereco de retorno
#  e seus parametros sao empilhados quando ele retorna desempilhados
#
# Empilhar -> Colocar um item de dados no topo da pilha
# Desempilhar -> Remover um item do topo da pilha
#
# O ultimo a entrar e o primeiro a sair
# LIFO - Last int First Out
